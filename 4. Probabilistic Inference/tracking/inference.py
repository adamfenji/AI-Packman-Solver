class ParticleFilter(InferenceModule):
    """
    A particle filter for approximately tracking a single ghost.
    """
    def __init__(self, ghostAgent, numParticles=300):
        InferenceModule.__init__(self, ghostAgent)
        self.setNumParticles(numParticles)

    def setNumParticles(self, numParticles):
        self.numParticles = numParticles


    # **************
    #  Question 5
    # ************** 
    def initializeUniformly(self, gameState):
        self.particles = []
        
        #no legal positions, do nothing
        if len(self.legalPositions) == 0: return

        for i in range(self.numParticles):
            self.particles.append(self.legalPositions[i % len(self.legalPositions)])


    # **************
    #  Question 6
    # ************** 
    def observeUpdate(self, observation, gameState):

        #calculate the weight for each particle
        weightedDistribution = DiscreteDistribution()
        for particle in self.particles:
            weightedDistribution[particle] += self.getObservationProb(observation, gameState.getPacmanPosition(), particle, self.getJailPosition())

        #if all weights are zero
        if weightedDistribution.total() == 0: self.initializeUniformly(gameState)
        else: self.particles = [weightedDistribution.sample() for _ in range(self.numParticles)]



    # **************
    #  Question 7
    # ************** 
    def elapseTime(self, gameState):
        newParticles = []

        # For each old particle, sample a new position
        for oldPosition in self.particles:
            newParticles.append(self.getPositionDistribution(gameState, oldPosition).sample())
        self.particles = newParticles #then, update the list of particles


    # **************
    #  Question 5
    # ************** 
    def getBeliefDistribution(self):
        beliefDistribution = DiscreteDistribution()
        
        for p in self.particles: # Count the occurrences
            beliefDistribution[p] += 1
        
        beliefDistribution.normalize() #from q0, now it's fixed
        return beliefDistribution


